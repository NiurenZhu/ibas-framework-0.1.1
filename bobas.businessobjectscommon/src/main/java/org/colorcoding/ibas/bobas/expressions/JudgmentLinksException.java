package org.colorcoding.ibas.bobas.expressions;


public class JudgmentLinksException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -3098369422805629747L;

	public JudgmentLinksException() {
	}

	public JudgmentLinksException(String message) {
		super(message);
	}

	public JudgmentLinksException(Throwable cause) {
		super(cause);
	}

	public JudgmentLinksException(String message, Throwable cause) {
		super(message, cause);
	}

	public JudgmentLinksException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
